public class Point2d {
    double x;
    double y;

    //默认构造函数
    public Point2d(){
        x = 0.0;
        y = 0.0;
    }

    //构造函数
    public  Point2d(double x0, double y0) {
        x = x0;
        y = y0;
    }
}